string = '21f.a/at/ss5os.ghs22s3'

d = {'Спец. символи': 0, 'Букви': 0, 'цифри': 0}
for i in string:
    if i.isalpha():
        d['букви'] += 1
    elif i.isdigit():
        d['цифри'] += 1
    else:
        d['Спец. символи'] += 1
print('Цифри = ',d['цифри'],'Букви = ' ,d['букви'],'Спец.символи = ' , d['Спец. символи'])                
